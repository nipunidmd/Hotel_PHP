                    <p>Column grid is a robust layout that is both responsive and nestable. It can accomodate upto 10 columns by using pre-defined widths. The columns are fully responsive in all devices. The following table shows an overview of the classes sizes you can use to create columns:</p>
                    <table class="uk-table">
                      <thead>
                        <tr>
                          <th class="uk-width-1-2">Class</th>
