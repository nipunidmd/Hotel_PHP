                        <tr>
                          <td><code>uk-width-1-10</code> to <code>uk-width-9-10</code></td>
                          <td>Divides the grid into tenths.</td>
                        </tr>
                        <tr>
                          <td><code>uk-grid uk-grid-width-1-4</code></td>
                          <td>Divides the elements of a grid into the number of columns specified using the uk-grid-width-* class. e.g. in this case the grid will be divided into four. You can also use 1, 2, 3, 4, 6 and 10.</td>
